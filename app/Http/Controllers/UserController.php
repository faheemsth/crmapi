    public function Pluck_All_Users_by_filter(Request $request)
    {
        // Validate input
        $validator = Validator::make($request->all(), [
            'filters' => 'nullable|array',
        ]);

        if ($validator->fails()) {
            return response()->json([
                'status'  => 'error',
                'message' => $validator->errors()
            ], 422);
        }

        $filters = $request->input('filters', []);
        $authUser = \Auth::user();

        // Excluded types
        $excludedTypes = ['company', 'team', 'client'];

        // Base user query
         

        $query = User::select('users.id', 'users.name');

         // dd($filters);

        // Apply excluded types ONLY when the filter does NOT include "type"
        if (!isset($filters['type'])) {
            $query->whereNotIn('type', $excludedTypes);
        }

        // Role conditions
        if ($authUser->type === 'super admin') {

            // super admin sees all (except excluded types)
           // $query->whereNotIn('type', $excludedTypes);

        } elseif ($authUser->type === 'company') {

            // Company sees only users under its brand
            $query->where('brand_id', $authUser->id);

        } else {

            // Team or other sees only same brand users
            $query->where('brand_id', $authUser->brand_id);
        }

        //dd($filters);

        // Apply dynamic filters only for valid columns
        if (!empty($filters)) {
            foreach ($filters as $column => $value) {

                if (!Schema::hasColumn('users', $column)) {
                    continue; // skip invalid column
                }

                if (is_array($value)) {
                    $query->whereIn($column, $value);
                } else {
                    $query->where($column, $value);
                }
            }
        }

       

        // Final user list
        $users = $query->pluck('name', 'id')->toArray();

        // Add extra user if not exist
        $extraUserId = "3257";

        if (!isset($users[$extraUserId])) {
            $users[$extraUserId] = "Shahzad Anwar";
        }

        return response()->json([
            'status' => 'success',
            'data'   => $users
        ], 200);
    }


    public function employees(Request $request)
    {
        $user = \Auth::user();

        // Ensure the user has permission to manage employees
        if (\Auth::user()->can('manage employee')) {
            $excludedTypes = ['company', 'team', 'client'];
            $usersQuery = User::select('users.*');

            // Get company filters
            $companies = FiltersBrands();
            $brand_ids = array_keys($companies);

            // Apply permissions based on user levels and attributes
            if (\Auth::user()->can('level 1')) {
                // Permissions for level 1
            } elseif (\Auth::user()->type == 'company') {
                $usersQuery->where('brand_id', \Auth::user()->id);
            } elseif (\Auth::user()->can('level 2')) {
                $usersQuery->whereIn('brand_id', $brand_ids);
            } elseif (\Auth::user()->can('level 3') && !empty(\Auth::user()->region_id)) {
                $usersQuery->where('region_id', \Auth::user()->region_id);
            } elseif (\Auth::user()->can('level 4') && !empty(\Auth::user()->branch_id)) {
                $usersQuery->where('branch_id', \Auth::user()->branch_id);
            } else {
                $usersQuery->where('id', \Auth::user()->id);
            }

            // Apply exclusion of user types
            $usersQuery->whereNotIn('type', $excludedTypes);

            // Fetch user data (e.g., 'name' and 'id')
            $users = $usersQuery->orderBy('users.name', 'ASC')->get(['name', 'id']);

            // Return response with status and data
            return response()->json([
                'status' => 'success',
                'data' => $users
            ]);
        }

        // Return an error if the user doesn't have permission
        return response()->json([
            'status' => 'error',
            'message' => 'Unauthorized'
        ], 403);
    }



