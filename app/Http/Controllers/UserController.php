    private function buildEmailData($template, $user,$cc=null)
    {
        if (!$template) {
            return [];
        }

        $subject = $this->replaceTags($template->subject, $user);
        $content = $this->replaceTags($template->template, $user);

        return [
            'to'           => $user->email,
            'cc'           => $cc,
            'subject'      => $subject,
            'brand_id'     => $user->brand_id,
            'from_email'   => $template->from ?? 'hr@scorp.co',
            'branch_id'    => $user->branch_id,
            'region_id'    => $user->region_id,
            'is_send'      => '0',
            'sender_id'    => 0,
            'created_by'   => 0,
            'priority'     => 1,
            'content'      => $content,
            'stage_id'     => null,
            'pipeline_id'  => null,
            'template_id'  => $template->id,
            'related_type' => 'employee',
            'related_id'   => $user->id,
            'created_at'   => now(),
            'updated_at'   => now(),
        ];
    }

    private function replaceTags($content, $user)
    {
        $tags = EmailTag::all();
        $replacePairs = [];

        foreach ($tags as $tag) {
            $key = '{' . $tag->tag . '}';
              // If tag exists as dynamic property on $user (from $additionalTags)
            if (isset($user->{$tag->tag})) {
                $value = $user->{$tag->tag};
            } else {
                $value = ''; // default
            }

            switch ($tag->tag) {
                case 'employee_name':
                    $value = $user->name;
                    break;
                case 'employee_email':
                    $value = $user->email;
                    break;
                case 'DOB':
                    $value = $user->date_of_birth;
                    break;

                case 'branch_manager_name':
                    $value = optional(optional($user->branch)->manager)->name ?? '';
                    break;
                case 'branch_manager_email':
                    $value = optional(optional($user->branch)->manager)->email ?? '';
                    break;

                case 'date_today':
                    $value = now()->toDateString();
                    break;
                case 'employee_designation':
                    $value = optional($user->designation)->name ?? '';
                    break;
                case 'commencedDate':
                    $value = optional(
                        $user->employeeMetas->where('meta_key', 'commencedDate')->first()
                    )->meta_value ?? '';
                    break;
                        // ðŸ”¹ Document info
                    case 'document_name':
                        $value = $user->document_name ?? '';
                        break;
                    case 'document_expiry':
                        $value = $user->document_expiry ?? '';
                        break;

                    // ðŸ”¹ Managers
                    case 'project_manager_name':
                        $value = $user->project_manager?->name ?? '';
                        break;
                    case 'project_manager_email':
                        $value = $user->project_manager?->email ?? '';
                        break;
                    }

            $replacePairs[$key] = $value;
        }

        return strtr($content, $replacePairs);
    }

    public function completeProfile(Request $request)
{
    

    // Validation for NEW business profile fields
    $validator = \Validator::make($request->all(), [
        'emp_id' => 'required|exists:users,id',

        // Name fields
        'fullname' => 'required|string|max:255', 

        // Business info
        'business_name' => 'required|string|max:500',
        'business_address' => 'nullable|string|max:500',

        // National ID
        'passport_number' => 'required|string|max:20', 

        // Business location
        'country' => 'required|string',
        'street_address' => 'required|string',
        'city' => 'required|string',
        'state' => 'required|string',
        'postal_code' => 'nullable|string',
    ]);

    if ($validator->fails()) {
        return response()->json([
            'status' => false,
            'message' => $validator->errors(),
        ], 400);
    }

    \DB::beginTransaction();
    try {

        $user = User::find($request->emp_id);

        if (!$user) {
            return response()->json([
                'status' => false,
                'message' => 'User not found.',
            ], 400);
        }

        // Save original values for logging
        $originalData = $user->toArray();

        // Update name
        $user->name = $request->fullname;

        // Update business profile fields
        $user->business_name = $request->business_name;
        $user->business_address = $request->business_address ?? $user->business_address;
        $user->passport_number = $request->passport_number;

        // Update address components
       // $user->country = $request->country;
        $user->country_id = $request->country;
        $user->Province = $request->state;
        $user->City = $request->city;
       $user->Postal = $request->postal_code ?? $user->Postal;
        $user->address = $request->street_address;
        $user->is_agent_profile_completed = 1;

        $user->save();

        // Detect changed fields
        $changes = [];
        foreach ($originalData as $field => $oldValue) {
            if (in_array($field, ['created_at','updated_at','password'])) continue;

            if ($user->$field != $oldValue) {
                $changes[$field] = [
                    'old' => $oldValue,
                    'new' => $user->$field
                ];
            }
        }

        if (!empty($changes)) {
            addLogActivity([
                'type' => 'info',
                'note' => json_encode([
                    'title' => $user->name." agent profile updated",
                    'message' => "Fields updated: " . implode(", ", array_keys($changes)),
                    'changes' => $changes
                ]),
                'module_id' => $user->id,
                'module_type' => 'agent_profile',
                'notification_type' => 'Profile Updated',
            ]);
        }

        \DB::commit();

        return response()->json([
            'status' => true,
            'message' => 'Profile updated successfully.',
            'data' => $user
        ]);

    } catch (\Exception $e) {
        \DB::rollBack();
        return response()->json([
            'status' => false,
            'message' => $e->getMessage()
        ], 500);
    }
}
